import mongoose from "mongoose"

const MessagesSchema = mongoose.Schema({
    senderId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    receiverId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    message: {
        type: String,
        required: true
    }
}, {
    timestamps: true
})

const Messages = mongoose.model("Messages", MessagesSchema)

export default Messages